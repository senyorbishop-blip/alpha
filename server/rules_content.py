def _apply_subclass_spell_lists(spells: List[Dict[str, Any]]) -> None:
    """Populate subclass_lists for Wizard spells based on D&D 5e subclass spell access rules.

    Eldritch Knight (Fighter subclass, id: "eldritch-knight"):
      - Gains Abjuration and Evocation wizard spells at level 3.
      - Gains any wizard spell at level 8+.

    Arcane Trickster (Rogue subclass, id: "arcane-trickster"):
      - Gains Enchantment and Illusion wizard spells at level 3.
      - Gains any wizard spell at level 8+.

    Because the level-8 rule grants access to ALL wizard spells for both subclasses,
    every spell with "Wizard" in class_lists receives both subclass IDs.
    The school-specific IDs are also added (they are deduplicated automatically).
    """
    _EK = "eldritch-knight"
    _AT = "arcane-trickster"
    _EK_SCHOOLS: set = {"Abjuration", "Evocation"}
    _AT_SCHOOLS: set = {"Enchantment", "Illusion"}

    for spell in spells:
        if not isinstance(spell, dict):
            continue
        class_lists = spell.get("class_lists") or []
        if "Wizard" not in class_lists:
            continue
        school = str(spell.get("school") or "")
        # An empty/missing school skips the school-specific checks below.
        # The spell still receives both subclass IDs via the level-8 rule (lines below).
        to_add: List[str] = []
        # School-restricted access (level 3+)
        if school in _EK_SCHOOLS:
            to_add.append(_EK)
        if school in _AT_SCHOOLS:
            to_add.append(_AT)
        # Level 8+ "any wizard spell" rule — both subclasses gain all wizard spells.
        to_add.append(_EK)
        to_add.append(_AT)
        # Merge into subclass_lists, preserving existing entries and deduplicating.
        existing: List[str] = list(spell.get("subclass_lists") or [])
        seen: set = set(existing)
        for sid in to_add:
            if sid not in seen:
                seen.add(sid)
                existing.append(sid)
        spell["subclass_lists"] = existing


